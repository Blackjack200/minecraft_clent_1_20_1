   private static void buildAllTabContents(CreativeModeTab.ItemDisplayParameters creativemodetab_itemdisplayparameters) {
      streamAllTabs().filter((creativemodetab3) -> creativemodetab3.getType() == CreativeModeTab.Type.CATEGORY).forEach((creativemodetab2) -> creativemodetab2.buildContents(creativemodetab_itemdisplayparameters));
      streamAllTabs().filter((creativemodetab1) -> creativemodetab1.getType() != CreativeModeTab.Type.CATEGORY).forEach((creativemodetab) -> creativemodetab.buildContents(creativemodetab_itemdisplayparameters));
   }

   public static boolean tryRebuildTabContents(FeatureFlagSet featureflagset, boolean flag, HolderLookup.Provider holderlookup_provider) {
      if (CACHED_PARAMETERS != null && !CACHED_PARAMETERS.needsUpdate(featureflagset, flag, holderlookup_provider)) {
         return false;
      } else {
         CACHED_PARAMETERS = new CreativeModeTab.ItemDisplayParameters(featureflagset, flag, holderlookup_provider);
         buildAllTabContents(CACHED_PARAMETERS);
         return true;
      }
   }
