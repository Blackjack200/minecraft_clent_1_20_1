   private static void generatePotionEffectTypes(CreativeModeTab.Output creativemodetab_output, HolderLookup<Potion> holderlookup, Item item, CreativeModeTab.TabVisibility creativemodetab_tabvisibility) {
      holderlookup.listElements().filter((holder_reference1) -> !holder_reference1.is(Potions.EMPTY_ID)).map((holder_reference) -> PotionUtils.setPotion(new ItemStack(item), holder_reference.value())).forEach((itemstack) -> creativemodetab_output.accept(itemstack, creativemodetab_tabvisibility));
   }

   private static void generateEnchantmentBookTypesOnlyMaxLevel(CreativeModeTab.Output creativemodetab_output, HolderLookup<Enchantment> holderlookup, Set<EnchantmentCategory> set, CreativeModeTab.TabVisibility creativemodetab_tabvisibility) {
      holderlookup.listElements().map(Holder::value).filter((enchantment1) -> set.contains(enchantment1.category)).map((enchantment) -> EnchantedBookItem.createForEnchantment(new EnchantmentInstance(enchantment, enchantment.getMaxLevel()))).forEach((itemstack) -> creativemodetab_output.accept(itemstack, creativemodetab_tabvisibility));
   }

   private static void generateEnchantmentBookTypesAllLevels(CreativeModeTab.Output creativemodetab_output, HolderLookup<Enchantment> holderlookup, Set<EnchantmentCategory> set, CreativeModeTab.TabVisibility creativemodetab_tabvisibility) {
      holderlookup.listElements().map(Holder::value).filter((enchantment2) -> set.contains(enchantment2.category)).flatMap((enchantment) -> IntStream.rangeClosed(enchantment.getMinLevel(), enchantment.getMaxLevel()).mapToObj((i) -> EnchantedBookItem.createForEnchantment(new EnchantmentInstance(enchantment, i)))).forEach((itemstack) -> creativemodetab_output.accept(itemstack, creativemodetab_tabvisibility));
   }

